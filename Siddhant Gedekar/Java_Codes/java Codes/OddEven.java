public class OddEven {
    public static void main(String[] args) {
        // Program to find odd and even
        for(int i=0;i<=20;i++){
            if(i%2==0){
                System.out.println("Even: "+i);
            }
            else{
                System.out.println("Odd:  "+i);
            }
        }
    }
}
